using System;
using System.Drawing;
using System.Windows.Forms;

namespace РГР
{
    public class HelpForm : Form
    {
        public HelpForm()
        {
            SetupHelpForm();
        }

        private void SetupHelpForm()
        {
            this.Text = "Справка — Визуализация алгоритмов сортировки";
            this.Size = new Size(700, 600);
            this.StartPosition = FormStartPosition.CenterParent;
            this.BackColor = Color.White;
            this.FormBorderStyle = FormBorderStyle.FixedDialog;
            this.MaximizeBox = false;
            this.MinimizeBox = false;

            RichTextBox richTextBox = new RichTextBox();
            richTextBox.Dock = DockStyle.Fill;
            richTextBox.ReadOnly = true;
            richTextBox.Font = new Font("Segoe UI", 10);
            richTextBox.BackColor = Color.White;
            richTextBox.Text = GetHelpText();

            this.Controls.Add(richTextBox);
        }

        private string GetHelpText()
        {
            return
"═══════════════════════════════════════════════════════════════\n" +
"         СПРАВКА — Визуализация алгоритмов сортировки          \n" +
"═══════════════════════════════════════════════════════════════\n\n" +

"1. НАЗНАЧЕНИЕ ПРОГРАММЫ\n" +
"━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n" +
"Данная программа предназначена для наглядной демонстрации работы\n" +
"основных алгоритмов сортировки целочисленных массивов.\n" +
"Вы можете видеть, как элементы сравниваются, перемещаются и\n" +
"постепенно выстраиваются по возрастанию.\n\n" +

"2. КАК ПОЛЬЗОВАТЬСЯ ПРОГРАММОЙ\n" +
"━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n" +
"1. Сгенерируйте массив – выберите размер массива (количество чисел),\n" +
"   затем нажмите кнопку «Новая выборка».\n" +
"2. Выберите алгоритм из списка (Пузырьковая, Быстрая, Слиянием и т.д.).\n" +
"3. Запустите сортировку – нажмите кнопку «Старт».\n" +
"4. Наблюдайте за анимацией:\n" +
"   - Сравниваемые элементы временно отображаются на левой панели.\n" +
"   - Затем они меняются местами или вставляются в правильную позицию.\n" +
"5. При необходимости можно поставить на паузу или сбросить анимацию.\n\n" +

"💡 Совет: для повторного запуска с тем же массивом используйте кнопку\n" +
"   «Сброс» (она возвращает массив в исходное состояние).\n\n" +

"ДОПОЛНИТЕЛЬНЫЙ ФУНКЦИОНАЛ:\n" +
"• Изменение скорости сортировки – ползунок «Скорость (мс)»\n" +
"• Изменение размера массива – NumericUpDown «Размер»\n\n" +

"3. ДОСТУПНЫЕ АЛГОРИТМЫ СОРТИРОВКИ\n" +
"━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n\n" +

"🔹 ПУЗЫРЬКОВАЯ (Bubble Sort)\n" +
"   Как работает: последовательно сравниваются соседние элементы.\n" +
"   Если порядок нарушен – они меняются местами.\n" +
"   Особенность: простой, но медленный на больших массивах (O(n²)).\n\n" +

"🔹 СОРТИРОВКА ВЫБОРОМ (Selection Sort)\n" +
"   Как работает: на каждом шаге ищется минимальный элемент в\n" +
"   неотсортированной части и меняется с первым неотсортированным.\n" +
"   Особенность: делает мало обменов, но много сравнений.\n\n" +

"🔹 СОРТИРОВКА ВСТАВКАМИ (Insertion Sort)\n" +
"   Как работает: массив постепенно строится из отсортированной части.\n" +
"   Каждый новый элемент вставляется в нужное место.\n" +
"   Особенность: очень эффективна на почти отсортированных данных.\n\n" +

"🔹 СОРТИРОВКА СЛИЯНИЕМ (Merge Sort)\n" +
"   Как работает: массив рекурсивно делится пополам, затем\n" +
"   отсортированные половинки сливаются воедино.\n" +
"   Особенность: быстрая (O(n log n)), но требует дополнительной памяти.\n\n" +

"🔹 БЫСТРАЯ СОРТИРОВКА (Quick Sort)\n" +
"   Как работает: выбирается опорный элемент, массив делится на две\n" +
"   части (меньше опорного и больше), затем каждая часть сортируется\n" +
"   рекурсивно.\n" +
"   Особенность: один из самых быстрых на практике (O(n log n) в среднем).\n\n" +

"🔹 ДРЕВЕСНАЯ СОРТИРОВКА (Tree Sort)\n" +
"   Как работает: элементы вставляются в двоичное дерево поиска,\n" +
"   затем обходятся по порядку.\n" +
"   Особенность: наглядно показывает древовидную структуру.\n\n" +

"4. ЛЕГЕНДА (ЦВЕТА ЭЛЕМЕНТОВ)\n" +
"━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n" +
"• Синий        – обычный элемент\n" +
"• Оранжевый    – элемент, участвующий в сравнении\n" +
"• Красный      – элемент, участвующий в обмене\n" +
"• Зелёный      – элемент уже на своём окончательном месте\n" +
"• Фиолетовый   – опорный элемент (для быстрой сортировки)\n\n" +

"5. ЧАСТЫЕ ВОПРОСЫ (FAQ)\n" +
"━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n" +
"Вопрос: Почему массив не сортируется за один шаг?\n" +
"Ответ: Программа показывает пошаговую анимацию, чтобы вы видели\n" +
"       логику алгоритма. Вы можете ускорить анимацию ползунком скорости.\n\n" +
"Вопрос: Что делать, если анимация зависла?\n" +
"Ответ: Нажмите «Сброс», затем снова «Старт».\n\n" +

"6. ГОРЯЧИЕ КЛАВИШИ\n" +
"━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n" +
"• F1 – вызов этой справки\n\n" +

"═══════════════════════════════════════════════════════════════\n" +
"                      © Визуализация сортировки v1.0\n" +
"═══════════════════════════════════════════════════════════════";
        }
    }
